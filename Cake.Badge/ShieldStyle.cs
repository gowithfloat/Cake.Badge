namespace Cake.Badge
{
    /// <summary>
    /// Options for shield styles.
    /// </summary>
    public enum ShieldStyle
    {
        /// <summary>
        /// The default shield style.
        /// </summary>
        Plastic,

        /// <summary>
        /// A flat style with no gradient.
        /// </summary>
        Flat,

        /// <summary>
        /// A flat style with square edges.
        /// </summary>
        FlatSquare,

        /// <summary>
        /// A really big badge.
        /// </summary>
        ForTheBadge,

        /// <summary>
        /// A badge in the style of a social button.
        /// </summary>
        Social,
    }
}
